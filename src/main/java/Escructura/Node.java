package Escructura;

/**
 *
 * @author kenie
 * @param <T>
 */
public class Node<T> {
    T data;
    Node<T> next;
    Node<T> prev;

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public Node<T> getNext() {
        return next;
    }

    public void setNext(Node<T> next) {
        this.next = next;
    }

    public Node<T> getPrev() {
        return prev;
    }

    public void setPrev(Node<T> prev) {
        this.prev = prev;
    }
    
   
    
}
